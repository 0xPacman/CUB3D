#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	long long	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
